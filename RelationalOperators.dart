// RELATIONAL OPERATORS.....
void main() {
  var a = 2 > 3; // Greater Than
  var b = 2 < 3; // Less Than
  var c = 2 <= 2; // Less than and equal to
  var d = 2 >= 4; // Greater than and equal to
  var e = 2 == 2; // Equivalency
  var f = 2 != 4; // Not Equal to
  print(a);
  print(b);
  print(c);
  print(d);
  print(e);
  print(f);
}
