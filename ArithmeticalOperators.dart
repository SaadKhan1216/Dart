// ARITHMETIC OPERATORS....
void main() {
  int a = 40;
  int b = 2;
  int c = 33;
  print(a + b); // ADDITION
  print(a - b); // SUBTRACTION
  print(a * b); // MULTIPLICATION
  print(a / b); // DIVISION
  print(c / b); // DIVISION
  print(c ~/ b); // ROUNDOFF DIVISION
  print(a % b); // REMAINDER
}
