// INT AND STRING CASES.........
void main() {
// USING A VARIABLE'S VALUE IN A PRINTED STATEMENT(USE OF $)....

  String a = 'Dart';
  String b = 'Flutter';
  print('${a} is based on ${b}');

// CONCATENATION IN STRING........

  String fname = 'Saad';
  String lname = 'Khan';
  print(fname + lname);

// USING AN INT VARIABLE'S VALUE IN A PRINTED STATEMENT(USE OF $)....
  int num1 = 20;
  int num2 = 30;
  print('${num1} + ${num2} = ${num1 + num2}');
}
